#include <stdlib.h>

int	ft_ultimate_range(int **range, int min, int max)
{
	int	i;
	int	result;

	i = 0;
	result = max - min;
	if (min >= max)
	{
		*range = NULL;
		return (0);
	}
	*range = malloc(sizeof(int) * (max - min));
	if (*range == NULL)
		return (0);
	while (min < max)
	{
		(*range)[i] = min;
		min ++;
		i++;
	}
	return (result);
}
