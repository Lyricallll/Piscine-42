#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	c;

	c = 1;
	i = 0;
	while (argv[c] && argc > 0)
	{
		i = 0;
		while (argv[c][i])
		{
			write(1, &argv[c][i], 1);
			i++;
		}
		write(1, "\n", 1);
		c++;
	}
}
